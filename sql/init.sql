DROP TABLE IF EXISTS `orders`;
CREATE TABLE `orders` (
    `id` INT NOT NULL AUTO_INCREMENT,
    `name` VARCHAR(255) NOT NULL DEFAULT '',
    `stu_id` VARCHAR(45) NOT NULL DEFAULT '',
    `date` DATETIME NOT NULL,
    `service_type` VARCHAR(255) NOT NULL DEFAULT '',
    `comment` LONGTEXT NOT NULL DEFAULT '',
    `rating` INT NOT NULL DEFAULT 0,
    `operater_id` INT NOT NULL,
    `secret_id` VARCHAR(255) NOT NULL DEFAULT '',
    `done_flag` BOOLEAN NOT NULL DEFAULT FALSE,
    `create_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    `update_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`));

